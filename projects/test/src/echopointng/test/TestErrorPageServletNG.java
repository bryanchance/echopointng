package echopointng.test;

import java.io.IOException;
import java.io.PrintWriter;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

/**
 * A servlet that will display an error page
 *
 */
public class TestErrorPageServletNG extends HttpServlet {
	/**
	 * @see javax.servlet.http.HttpServlet#service(javax.servlet.http.HttpServletRequest, javax.servlet.http.HttpServletResponse)
	 */
	protected void service(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		HttpSession session = request.getSession();
		Throwable t = (Throwable) session.getAttribute("echopointng.servletThrowable");
		PrintWriter out = new PrintWriter(response.getWriter());
		out.println("<html><head><title>Error!</title></head><body><pre>");
		t.printStackTrace(out);
		out.println("</pre></body></html>");
	}
}
